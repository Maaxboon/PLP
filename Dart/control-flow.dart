// if statement: this simple decision-make runs a block of code if a condition is true
void main(){
    var age = 16;

    if (age > 16) {
        print("Kyle is eligible to start driving classes in Kenya");
    } else if (age == 16) {
        print("Kyle is old enough to start driving in Kenya!");
    }else{
        print("Kyle is too young to start driving in Kenya");
    }
}